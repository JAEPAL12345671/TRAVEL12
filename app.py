# ============================================================
# app.py
# 이 파일은 "사진을 찍은 날짜"와 "어떤 기계에서 찍었는지"를
# 기준으로 사진을 정리해서 저장해주는 스트림릿(Streamlit) 앱이에요.
#
# 스트림릿(Streamlit)이란?
# -> 파이썬 코드만으로 웹사이트(홈페이지)를 아주 쉽게 만들 수 있게
#    도와주는 도구예요. 우리가 st.버튼() 이렇게 쓰면
#    진짜로 화면에 버튼이 생겨요!
# ============================================================


# ------------------------------------------------------------
# 1. 필요한 도구들을 가져오기 (import)
# ------------------------------------------------------------
# "import"는 "이 상자에 들어있는 도구들을 빌려올게!" 라는 뜻이에요.

import streamlit as st
# streamlit : 웹 화면(버튼, 글자, 사진 등)을 만들어주는 도구 상자예요.
# as st 는 "이제부터 streamlit을 st라는 짧은 별명으로 부를게!" 라는 뜻이에요.
# 매번 streamlit이라고 길게 쓰기 귀찮으니까 짧게 줄인 거예요.

import os
# os : "운영체제(컴퓨터)와 대화하는 도구"예요.
# 폴더(디렉터리)를 만들거나, 폴더 안에 무엇이 있는지 확인할 때 써요.

from datetime import date
# datetime이라는 큰 상자에서 date(날짜)라는 작은 도구만 콕 집어서 가져왔어요.
# 오늘 날짜가 몇 년, 몇 월, 며칠인지 알려주는 도구예요.

from pathlib import Path
# Path : 컴퓨터 안의 "파일 위치(주소)"를 다루기 쉽게 도와주는 도구예요.
# 예를 들어 "photos/2026-07-21/1번기계" 같은 주소를
# 훨씬 안전하고 편하게 만들 수 있게 해줘요.


# ------------------------------------------------------------
# 2. 사진을 저장할 "기본 폴더" 이름 정하기
# ------------------------------------------------------------

BASE_FOLDER = "photos"
# BASE_FOLDER는 "모든 사진이 들어갈 큰 서랍장 이름"이라고 생각하면 돼요.
# 대문자로 쓴 이유는 "이 값은 앞으로 안 바뀌는 고정된 값이에요!"
# 라는 걸 사람들에게 알려주는 약속(습관) 같은 거예요.


# ------------------------------------------------------------
# 3. 웹페이지의 기본 설정을 하는 부분
# ------------------------------------------------------------

st.set_page_config(
    page_title="날짜별·기계별 사진 정리함",  # 브라우저 탭에 보이는 제목
    page_icon="📷",                          # 브라우저 탭에 보이는 작은 아이콘(그림)
    layout="centered",                        # 화면 내용을 가운데로 정렬해요
)
# set_page_config는 "이 웹페이지의 겉모습 기본값"을 정해주는 함수예요.
# 우리가 사진관 간판을 다는 것과 비슷해요.

st.title("📷 날짜별 · 기계별 사진 저장함")
# st.title() : 화면에 아주 큰 제목 글씨를 보여줘요.

st.write("사진을 올리고, 어떤 기계에서 찍었는지 선택한 뒤 저장 버튼을 누르세요!")
# st.write() : 화면에 그냥 평범한 글자를 보여줘요. (설명, 안내 문구 등)


# ------------------------------------------------------------
# 4. 폴더를 자동으로 만들어주는 함수 만들기
# ------------------------------------------------------------
# "함수"는 "여러 번 쓸 일을 미리 상자에 담아둔 것"이에요.
# 매번 같은 코드를 다시 쓰지 않고, 함수 이름만 부르면 돼요.

def make_save_folder(selected_date, machine_name):
    # def는 "define(정의하다)"의 줄임말이에요. "이제부터 이런 함수를 만들게!" 라는 뜻이에요.
    # selected_date : 사용자가 고른 날짜가 여기로 들어와요.
    # machine_name : 사용자가 고른(또는 입력한) 기계 이름이 여기로 들어와요.

    folder_path = Path(BASE_FOLDER) / str(selected_date) / machine_name
    # Path(BASE_FOLDER) : "photos"라는 주소를 만들어요.
    # 뒤에 / str(selected_date) 를 붙이면 "photos/2026-07-21" 처럼
    # 주소 뒤에 폴더 이름을 계속 이어 붙일 수 있어요.
    # 마치 레고 블록을 계속 쌓아 올리는 것과 같아요!
    # str()을 쓴 이유는 날짜(date)를 "글자(문자)"로 바꿔줘야
    # 폴더 이름으로 쓸 수 있기 때문이에요.

    folder_path.mkdir(parents=True, exist_ok=True)
    # mkdir() : "make directory(폴더 만들기)"의 줄임말이에요.
    # parents=True : 중간에 필요한 폴더가 없으면 다 같이 만들어줘요.
    #   (예: "photos" 폴더가 없어도 자동으로 같이 만들어줌)
    # exist_ok=True : "이미 폴더가 있으면 에러 내지 말고 그냥 넘어가!" 라는 뜻이에요.

    return folder_path
    # return : "이 함수가 일을 끝내고 나서 돌려줄 결과물"이에요.
    # 여기서는 방금 만든 폴더의 주소를 돌려줘요.


# ------------------------------------------------------------
# 5. 사용자가 입력할 수 있는 화면(입력창) 만들기
# ------------------------------------------------------------

st.subheader("1️⃣ 사진 정보를 입력하세요")
# st.subheader() : title보다는 조금 작은 소제목 글씨예요.

# --- 날짜 선택하기 ---
selected_date = st.date_input(
    "촬영한 날짜를 선택하세요",  # 입력창 위에 보이는 안내 글자
    value=date.today(),          # 처음 화면에 보일 기본값 = 오늘 날짜
)
# st.date_input() : 달력 모양으로 날짜를 고를 수 있는 입력창을 만들어줘요.
# value=date.today() 는 "일단 오늘 날짜를 기본으로 보여줘!" 라는 뜻이에요.

# --- 기계 이름 선택하기 ---
machine_list = ["1번 기계", "2번 기계", "3번 기계", "직접 입력"]
# machine_list : 자주 쓰는 기계 이름들을 미리 목록(리스트)으로 만들어 둔 거예요.
# 리스트는 대괄호 [ ] 를 사용해서 여러 개의 값을 한 줄로 모아둔 상자예요.

selected_machine = st.selectbox(
    "기계를 선택하세요",  # 안내 글자
    machine_list,          # 선택할 수 있는 목록
)
# st.selectbox() : 여러 개 중 하나만 고를 수 있는 "드롭다운(선택) 메뉴"를 만들어줘요.

# 만약 "직접 입력"을 골랐다면, 이름을 새로 타이핑할 수 있게 해줘요.
if selected_machine == "직접 입력":
    # if : "만약 ~라면" 이라는 뜻이에요. 조건을 확인할 때 써요.
    # ==  는 "같다(=)"를 비교하는 기호예요. (하나만 쓰면 "저장하다"라는 뜻이 되어버려서
    # 두 개를 붙여서 == 로 써야 "같은지 비교하기"가 돼요.)

    machine_name = st.text_input("기계 이름을 직접 입력하세요")
    # st.text_input() : 사람이 직접 글자를 타이핑할 수 있는 입력창이에요.
else:
    # else : "만약 위 조건이 아니라면" 이라는 뜻이에요.
    machine_name = selected_machine
    # 목록에서 고른 이름을 그대로 사용해요.


# ------------------------------------------------------------
# 6. 사진 파일 올리는(업로드) 부분 만들기
# ------------------------------------------------------------

st.subheader("2️⃣ 사진 파일을 올리세요")

uploaded_files = st.file_uploader(
    "사진을 선택하세요 (여러 장 선택 가능)",
    type=["jpg", "jpeg", "png"],  # 이 종류의 파일만 올릴 수 있게 제한해요.
    accept_multiple_files=True,    # True로 하면 여러 장을 한 번에 올릴 수 있어요.
)
# st.file_uploader() : 컴퓨터 안에 있는 파일을 웹페이지로 가져올 수 있는
# "파일 선택 버튼"을 만들어줘요.
# type=[...] : "이런 확장자를 가진 파일만 받을게!" 라는 규칙이에요.
# 사진이 아닌 이상한 파일이 올라오는 걸 막아줘요.


# ------------------------------------------------------------
# 7. 저장 버튼을 누르면 실제로 사진을 저장하는 부분
# ------------------------------------------------------------

if st.button("💾 사진 저장하기"):
    # st.button() : 눌렀을 때만 True(참)가 되는 버튼을 만들어줘요.
    # 즉, 이 버튼을 누른 순간에만 아래 코드가 실행돼요.

    if not machine_name:
        # not machine_name : "machine_name이 비어있다면(글자가 하나도 없다면)"
        # 이라는 뜻이에요. 기계 이름을 안 적었을 때를 대비한 거예요.
        st.warning("⚠️ 기계 이름을 입력해주세요!")
        # st.warning() : 노란색 경고 메시지를 화면에 보여줘요.

    elif not uploaded_files:
        # elif : "위 조건이 아니고, 대신 이 조건이라면" 이라는 뜻이에요.
        # (else + if를 합친 말이에요)
        st.warning("⚠️ 저장할 사진을 먼저 선택해주세요!")

    else:
        # 기계 이름도 있고, 사진도 있을 때만 이 부분이 실행돼요.

        save_folder = make_save_folder(selected_date, machine_name)
        # 아까 만든 함수를 불러서, 오늘 저장할 폴더를 만들어요.
        # 예: photos/2026-07-21/1번 기계

        saved_count = 0
        # saved_count : 사진을 몇 장 저장했는지 세는 숫자예요. 처음엔 0장이에요.

        for photo in uploaded_files:
            # for ~ in ~ : "uploaded_files 안에 있는 사진들을 하나씩 꺼내서
            # photo라는 이름을 붙여줄게!" 라는 뜻이에요. (반복문)
            # 사진이 5장이면 이 아래 코드가 5번 반복돼요.

            file_path = save_folder / photo.name
            # save_folder 주소 뒤에 사진 파일 이름을 붙여서
            # "최종 저장 위치"를 만들어요.
            # 예: photos/2026-07-21/1번 기계/사진1.jpg

            with open(file_path, "wb") as f:
                # open() : 파일을 새로 만들거나 여는 명령이에요.
                # "wb"는 write(쓰기) + binary(사진 같은 이진 데이터)라는 뜻이에요.
                # 사진은 글자가 아니라서 "wb" 모드로 저장해야 깨지지 않아요.
                # with ~ as f : "f라는 이름으로 파일을 열고,
                # 이 코드 블록이 끝나면 자동으로 파일을 안전하게 닫아줘!" 라는 뜻이에요.

                f.write(photo.getbuffer())
                # photo.getbuffer() : 업로드된 사진의 실제 내용(데이터)을 가져와요.
                # f.write() : 그 내용을 방금 연 파일 안에 써 넣어요(저장해요).

            saved_count += 1
            # saved_count += 1 은 "saved_count = saved_count + 1"과 같아요.
            # 사진을 하나 저장할 때마다 숫자를 1씩 늘려주는 거예요.

        st.success(f"✅ 사진 {saved_count}장을 '{save_folder}' 폴더에 저장했어요!")
        # st.success() : 초록색 성공 메시지를 보여줘요.
        # f"..." 는 "f-string"이라고 부르는데, 문장 중간에
        # {변수} 를 넣으면 그 변수의 실제 값으로 바뀌어서 보여줘요.


# ------------------------------------------------------------
# 8. 저장된 사진들을 날짜별 · 기계별로 화면에 보여주는 부분
# ------------------------------------------------------------

st.subheader("3️⃣ 저장된 사진 모아보기")

base_path = Path(BASE_FOLDER)
# 아까 만든 "photos" 폴더의 주소를 다시 가져와요.

if not base_path.exists():
    # .exists() : "이 폴더(주소)가 실제로 있나요?"를 True/False로 알려줘요.
    # not 을 붙였으니 "폴더가 없다면" 이라는 뜻이에요.
    st.info("아직 저장된 사진이 없어요. 위에서 먼저 사진을 저장해보세요!")
    # st.info() : 파란색 안내 메시지를 보여줘요.

else:
    # photos 폴더가 이미 있다면, 안에 들어있는 것들을 하나씩 보여줄게요.

    date_folders = sorted(base_path.iterdir())
    # base_path.iterdir() : "photos 폴더 안에 있는 것들을 하나씩 꺼내서 보여줘!"
    # sorted() : 그것들을 순서대로(오래된 날짜부터) 정렬해줘요.

    for date_folder in date_folders:
        # 날짜 폴더 하나씩(예: 2026-07-20, 2026-07-21 ...) 반복해요.

        if date_folder.is_dir():
            # .is_dir() : "이게 폴더가 맞나요?"를 확인해요.
            # (혹시 이상한 파일이 섞여 있을 경우를 대비한 안전장치예요.)

            with st.expander(f"📅 {date_folder.name}"):
                # st.expander() : 클릭하면 펼쳐지고, 다시 클릭하면 접히는
                # "접었다 폈다 하는 상자"를 만들어줘요.
                # 날짜별로 상자를 만들어서 화면이 너무 길어지지 않게 해줘요.

                machine_folders = sorted(date_folder.iterdir())
                # 그 날짜 폴더 안에 있는 기계 폴더들을 순서대로 가져와요.

                for machine_folder in machine_folders:
                    # 기계 폴더 하나씩(예: 1번 기계, 2번 기계 ...) 반복해요.

                    if machine_folder.is_dir():
                        st.markdown(f"**🛠️ {machine_folder.name}**")
                        # st.markdown() : 글자를 조금 꾸며서 보여줄 수 있어요.
                        # **글자** 로 감싸면 "굵은 글씨"가 돼요.

                        photo_files = list(machine_folder.glob("*"))
                        # glob("*") : "이 폴더 안에 있는 모든 파일 이름을 다 가져와줘!"
                        # 라는 뜻이에요. * 는 "아무거나 다"를 의미하는 기호예요.

                        columns = st.columns(4)
                        # st.columns(4) : 화면을 4개의 세로 칸으로 나눠줘요.
                        # 사진을 한 줄에 4장씩 나란히 보여주기 위해서예요.

                        for index, photo_file in enumerate(photo_files):
                            # enumerate() : 반복하면서 "몇 번째인지 숫자(index)"도
                            # 같이 알려주는 도구예요. (0, 1, 2, 3, 0, 1, 2, 3 ...)

                            with columns[index % 4]:
                                # index % 4 : index를 4로 나눈 "나머지"를 구해요.
                                # 나머지는 항상 0,1,2,3 중 하나이기 때문에
                                # 사진이 아무리 많아도 4칸 안에서 계속 순서대로
                                # 채워지게 만들 수 있어요.

                                st.image(str(photo_file), use_container_width=True)
                                # st.image() : 화면에 실제 사진을 보여줘요.
                                # use_container_width=True : 사진 크기를
                                # 칸 너비에 딱 맞게 자동으로 조절해줘요.


# ------------------------------------------------------------
# 9. 배포할 때 참고할 점 (주의 안내)
# ------------------------------------------------------------
# 이 앱을 Streamlit Community Cloud(streamlit.io)에 배포하면,
# 서버가 가끔 다시 시작(재시작)될 수 있는데, 그때 이 "photos" 폴더 안의
# 사진들이 사라질 수 있어요. (임시 저장 공간이기 때문이에요.)
# 그래서 정말 중요한 사진이라면, 나중에는 구글 드라이브, AWS S3 같은
# "진짜로 오래 보관해주는 저장소"와 연결하는 걸 추천해요.
# 지금은 처음 배우는 단계이니, 우선 이렇게 로컬(내 컴퓨터/서버) 폴더에
# 저장하는 방식으로 시작하면 충분해요!
